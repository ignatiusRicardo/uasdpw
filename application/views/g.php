	<h2>Saul Aaron Kripke <br/><br/> Mengajar Havard saat Masih Duduk di Bangku SMA</h2>

	<p>
	Lahir di New York dan tumbuh dewasa di Omaha di 1940. Jenius satu ini, saat kelas empat ia menguasai aljabar, saat akhir SD ia sudah bisa geometri, kalkulus dan filsafat. Saat SMU ia memperoleh surat dari Harvard agar melamar sebagai dosen, namun ibunya menyuruhnya untuk menamatkan sekolahnya dahulu.<br/>
Kripke dihadiahi Schock Prize, Nobel Filosofi. Sekarang, ia dinobatkan sebagai ahli filsafat terbesar dalam sejarah</p>
