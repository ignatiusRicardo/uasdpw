	<h2>Aelita Andre <br/><br/> Pelukis di usia 2 tahun</h2>

	<p>
	Anak kelahiran Australia ini, baru berumur dua tahun sudah menunjukkan kualitasnya sebagai jenius, ia memiliki sebuah gedung pertunjukkan untuk karya-karya abstraknya.<br/>
Pada mulanya Mark Jamieson, direktur dari Brunswick Street Gallery di Melbourne's Fitzroy. Tretarik melihat sebuah poto lukisan dari Aelita Andre. dan dia menginginkannya bergabung dalam grupnya karena bakat lukisannya itu. Ketika undangan telah dibuat, ia baru saja menyadari bahwa Aelita adalah anak yang masih berumur 22 bulan. Namun ia tetap melanjutkan pertunjukannya itu.</p>
