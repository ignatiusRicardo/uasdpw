	<h2>Akrit Jaswal <br/><br/> Dokter Bedah usia 7 tahun</h2>

	<p>
	Julukan "anak terpandai di dunia" telah melekat pada Akrit Jaswal, seoarang anak dari India. Ia mengejutkan Publik, ketika pada umur 7 tahun melakukan pembedahan pada seorang gadis lokal di tempatnya. gadis itu menderita luka bakar di tangannya, hingga tangannya tidak dapat dibuka, dan jaswalpun melakukan pembedahan hingga jemari gadis itu bisa terbuka seperti sedia kala.<br/>
Saat ini, ia tercatat sebagai dokter paling muda di dunia, ia diterima di Universitas pada usia 11 tahun.</p>
