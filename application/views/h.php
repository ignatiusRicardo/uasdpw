	<h2>Willie Mosconi : Pemain Bilyard Professional pada usia 6</h2>

	<p>
	Mendapat julukan “Tuan Pocket Billiards”. Dia berasal dari Philadelphia, Pennsylvania. Ayahnya seorang pemilik tempat Billyard, namun ayahnya tidak mengizinkannyamain, ia sering berimprovisasi dengan gagang sapu milik ibunya. Ayahnya melihat bakat anaknya, ia sering mengalahkan orang-orang yang lebig tua darinya.<br/>
Antara tahun 1941 dan 1957, dia memenagkan BCA World Championship selama 15 kali tanpa perbah kalah sekalipun. ia memebuat berbagai macam trik, membuat banyak rekor, dan membuat billyard menjadi olahraga yang terkenal</p>
